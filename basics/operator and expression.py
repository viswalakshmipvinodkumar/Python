#arithmentic operator

#addition
a = 10
b = 5
print("sum:", a + b)
#subtraction
print("difference:", a - b)
#multiplication
print("product:", a * b)
#division
print("quotient:", a / b)

#modulus
print("remainder:", a % b)
#Floor Division
print("floor division:", a // b)
#exponentiation
print("exponentiation:", a ** b)

#relational operator

x = 15
y = 10  
#greater than
print("x > y:", x > y)
#less than
print("x < y:", x < y)
#greater than or equal to
print("x >= y:", x >= y)
#less than or equal to      
print("x <= y:", x <= y)
#equal to
print("x == y:", x == y)
#not equal to
print("x != y:", x != y)

#logical operator

